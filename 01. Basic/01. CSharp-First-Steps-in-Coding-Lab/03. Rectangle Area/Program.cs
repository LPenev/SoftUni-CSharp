namespace _03._Rectangle_Area
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Variables
            int a, b, area;
            // Input a
            a = int.Parse(Console.ReadLine());
            // Input b
            b = int.Parse(Console.ReadLine());
            // Calculate a*b
            area = a * b;
            // Show Result
            Console.WriteLine(area);
        }
    }
}
